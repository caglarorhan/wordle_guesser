    noteIt:function (text){
        if(!this.notesItemId || !text || !document.querySelector(`#${this.notesItemId}`)) return;
        document.querySelector(`#${this.notesItemId}`).innerHTML=text;
    },
    createLetterPositionMap:function (wordList){
        let letterPositionMap={};
        wordList.forEach(word=>{
            word.split('').forEach((letter,index)=>{
                if(!letterPositionMap[letter]){
                    letterPositionMap[letter]={};
                }
                if(letterPositionMap[letter][index]){
                    letterPositionMap[letter][index]++
                }else{
                    letterPositionMap[letter][index]=1
                }
            })
        })
        Object.keys(letterPositionMap).forEach(letter=>{
            for(let x=0;x<this.wordLength;x++){
                if(!letterPositionMap[letter][x]){
                    letterPositionMap[letter][x]=0;
                }
            }
        })
        return letterPositionMap;
    },
    createWordsMapByLetterPositionsValues:function (wordList){
        let wordsMap =[]
        let letterPositionMap = this.createLetterPositionMap(this.upperCaseWords)
        wordList.forEach(word=>{
            let wordLettersPositionsTotalValue=0;
            word.split('').forEach((letter,index)=>{
                wordLettersPositionsTotalValue+=letterPositionMap[letter][index]
            })
            wordsMap.push({word:word, value:wordLettersPositionsTotalValue});
        })
        return wordsMap.sort((a,b)=>{return b.value-a.value});
    },
    stepByStepFilter:function (query,wordList){
        switch (query.status){
            case 'correct':
                WG.sendMessageToContent({type:'plainMessageFromPopup',payload:{message:`Ayiklama oncesi liste ${wordList}`}})
                WG.sendMessageToContent({type:'plainMessageFromPopup',payload:{message:`Query: ${JSON.stringify(query)}`}})
                wordList = wordList.filter(word=>word.charAt(query.position)===query.letter);
                WG.sendMessageToContent({type:'plainMessageFromPopup',payload:{message:`Ayiklama sonrasi liste ${wordList}`}})
                break
            case 'present':
                WG.sendMessageToContent({type:'plainMessageFromPopup',payload:{message:`Ayiklama oncesi liste ${wordList}`}})
                WG.sendMessageToContent({type:'plainMessageFromPopup',payload:{message:`Query: ${JSON.stringify(query)}`}})
                if(query.isMultiple){
                    wordList = wordList.filter(word=>(word.charAt(query.position)!==query.letter && word.split('').filter(letter=>letter===query.letter).length>1));
                }else{
                    wordList = wordList.filter(word=>(word.charAt(query.position)!==query.letter && word.includes(query.letter)));
                }
                WG.sendMessageToContent({type:'plainMessageFromPopup',payload:{message:`Ayiklama sonrasi liste ${wordList}`}})
                break
            case 'absent':
                WG.sendMessageToContent({type:'plainMessageFromPopup',payload:{message:`Ayiklama oncesi liste ${wordList}`}})
                WG.sendMessageToContent({type:'plainMessageFromPopup',payload:{message:`Query: ${JSON.stringify(query)}`}})
                wordList = wordList.filter(word=>(word.charAt(query.position)!==query.letter));
                WG.sendMessageToContent({type:'plainMessageFromPopup',payload:{message:`Ayiklama sonrasi liste ${wordList}`}})
                break
            default:
                WG.sendMessageToContent({type:'plainMessageFromPopup',payload:{message:'Unknown status of query'}})
        }
        return wordList;
    },
    queryFilterStepper:function (queries, wordList){
        queries.forEach(query=>{
            wordList = this.stepByStepFilter(query,wordList);
        })
        return wordList;
    },
    sendMessageToContent:function (messageObject){
        // {type:type,payload:payload}
        this.noteIt('Sending mesaage to content');
        chrome.tabs.query({active: true, currentWindow: true}, function (tabs) {
            chrome.tabs.sendMessage(tabs[0].id, messageObject, function (response) {
                WG.noteIt('Message sent to content.');
            });
        });
    },
    sendNewWord:function (word=''){
        WG.sendMessageToContent({type:'plainMessageFromPopup',payload:{message:`New word sent: ${word}`}})
        this.noteIt('New word is being sent to content: '+word);
        this.sendMessageToContent({type:'newWord',payload:{word:word}});
    },
