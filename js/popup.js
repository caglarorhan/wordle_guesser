    consecutiveWordsThatHasNonRepeatingLetters: (wordList)=>{
        let wordsMapByLetterPositionsValues = WG.createWordsMapByLetterPositionsValues(wordList);
        let uniqueLetterList =[];
        let uWords=[]
        wordsMapByLetterPositionsValues.forEach(wordObject=>{
            let lettersOfWord = wordObject.word.split('');
            let uniqueLettersOfWord = new Set(lettersOfWord)
            if(lettersOfWord.length===[...uniqueLettersOfWord].length){
                //------------------------------------------
                let isUnique = true;
                for(let x=0; x<lettersOfWord.length; x++){
                    if(uniqueLetterList.includes(lettersOfWord[x])){
                        isUnique = false;
                    }
                }
                if(isUnique){
                    uniqueLetterList = uniqueLetterList.concat(lettersOfWord);
                    uWords.push(wordObject);
                }
                //------------------------------------------
            }
        })
        return uWords;
    },
