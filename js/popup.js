chrome.runtime.onMessage.addListener(  async (request)=>{
    WG.sendMessageToContent({type:'plainMessageFromPopup',payload:{message:`Words On The Board: ${JSON.stringify(request.payload.wordsOnBoard)}`}})
    switch(request.type){
        case 'getNewWord':
            if(request.payload.queries){
                let filteredWordList = await WG.queryFilterStepper(request.payload.queries, WG.upperCaseWords);
                filteredWordList = filteredWordList.filter(word=>request.payload.wordsOnBoard.indexOf(word)===-1);
                let newWordGuessByValuePoints = await WG.createWordsMapByLetterPositionsValues(filteredWordList)[0];
                WG.sendNewWord(newWordGuessByValuePoints.word);
            }else{
                let newWordGuessByValuePoints = WG.createWordsMapByLetterPositionsValues(WG.upperCaseWords)[0];
                WG.sendNewWord(newWordGuessByValuePoints.word);
            }
            break;
    }
})
